#include "libft.h"

int		ft_sqrt(int	n)
{
	int		sqrt;

	sqrt = 0;
	while (sqrt * sqrt <= n)
	{
		if (sqrt * sqrt == n)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
