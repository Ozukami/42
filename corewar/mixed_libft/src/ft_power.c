#include "libft.h"

int		ft_power(int n, int p)
{
	int nmem;

	nmem = n;
	if (p == 1)
		return (n);
	if (p == 0)
		return (1);
	while (p > 1)
	{
		n = n * nmem;
		p--;
	}
	return (n);
}
